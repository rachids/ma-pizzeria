<h2>Règles du jeu</h2>

<p>Vous gérez une pizzeria.</p>

<h3>But du jeu</h3>
<p>Vous devez gérer votre restaurant. Gagnez de l'argent en vendant vos pizzas et en améliorant votre restau.<br/>
Achetez les ingrédients, employez les meilleurs pizzaïolos, équipez vous du meilleur matos et s'il le faut,
envoyez des voyous saccager la concurrence.</p>

<h3>Déroulement du jeu</h3>
<p>Lorsque vous commencez le jeu, vous avez un restaurant de base ainsi qu'une protection contre le banditisme.
Personne ne peut vous attaquer jusqu'au premier palier de bénéfice.<br/>
Vous allez devoir créer vos recettes et acheter les ingrédients nécessaires à leur fabrication, puis recruter vos
premiers salariés.</p>

<p>Les clients arrivent à deux moments de la journée : pour le déjeuner et pour le dîner. Concrètement, vous avez
deux périodes de rush. Tout est calculé automatiquement à 14h et à 00h (minuit). Le jeu calcule le nombre de clients
que vous avez attiré, combien de pizzas vendues, recalcule les stocks, paie les salaires, gère les voyous etc. C'est
important à savoir notamment pour la gestion des stocks d'ingrédients qui doit être renouveler au risque de voir ses
ventes chuter et sa notoriété tombée.</p>

<h3>Les ingrédients</h3>
<p>Une pizza nécessite de ingrédients. Pour la pâte vous n'aurez pas beaucoup de choix, mais pour la garniture, c'est à
vous de réaliser la pizza parfaite. Il faudra tester et retester pour trouver la recette qui fait mouche. (Notez qu'il
n'existe aucune recette parfaite et que tous les goûts sont dans la nature = les clients n'aimeront pas toujours les mêmes
choses).</p>
<p>Les ingrédients que vous n'utilisez pas sont stockés mais attention, chaque jour à minuit, vous perdez une partie
des ingrédients non utilisés (périmés), il est donc important de bien gérer ses stocks.</p>

<h3>Les pizzas</h3>
<p>A partir des ingrédients que vous acheter, vous pouvez créer vos pizzas et y ajouter les ingrédients que vous souhaitez.
Vous ne pouvez avoir que 3 recettes différentes, mais vous pouvez les modifier à tout moment.</p>
<p>La réalisation d'une recette de pizza se décompose en trois parties : d'abord son nom, il doit être unique et original.
Puis sa garniture, quels ingrédients la composeront et enfin une courte description, l'histoire de votre recette en bref.</p>
<p>La description et le nom sont importants car ils seront utilisés pour déterminer le top 3 des meilleurs pizzas du mois.
Les gagnants remportent un lot qui peut être une somme d'argent, un boost de notoriété ou autre chose décidé par l'admin.</p>

<h3>Les employés</h3>
<p>Vous êtes manager et avez le sens des affaires, mais laissez faire de véritables pro pour la fabrication de vos pizzas.<br/>
Entourez vous des meilleurs. Vous pouvez recruter des pizzaïolos, qui cuisineront vos pizzas; des serveurs, qui
s'occuperont des clients en salle et des livreurs, qui emmeneront la marchandise au domicile du client.</p>

<p>Vos salariés ont un niveau d'expérience et également un salaire. Ce salaire augmentera automatiquement avec leur
expérience. Il est donc important de garder un oeil sur les compétences de vos employés afin de ne pas se retrouver
avec des salaires trop gros. Il est parfois préférable de se séparer d'un bon cuisto plutôt que de payer un talent
inexploité.</p>

<p>Vous pouvez également avoir recours à des voyous. Contre une somme d'argent variable selon la nature du service
demandé, vous pouvez mettre des bâtons dans les roues de vos concurrents. Mais attention ! Si jamais le voyou échoue
dans sa tâche, vous risquez de devoir payer une amende et de voir votre notoriété baisser.</p>

<h3>Les clients</h3>
<p>Les clients débarquent dans votre restaurant deux fois par jour. Le nombre de client qui rend visite à votre restaurant
est aléatoire et dépend fortement de votre notoriété et des prix pratiqués.</p>
<p>De manière très très aléatoire, il est possible qu'une célébrité visite votre restaurant. Si vous avez la chance que
cela vous arrive, vous bénéficierez d'un boost de notoriété qui s'estompera au bout de quelques jours mais qui vous
permettra d'augmenter votre chiffre d'affaire !</p>

<h3>Les livraisons</h3>
<p>Vos clients sont parfois chez eux et attendent leur pizza. Toute les pizzerias offrent gratuitement la pizza si la
livraison dépasse les 30 minutes. C'est pourquoi il est important d'avoir de bons livreurs et un bon équipement.</p>
<p>Vous pouvez demander à vos livreurs de respecter ou non le code de la route. Mais attention, si jamais un livreur
se fait prendre à transgresser les règles de conduite, vous devrez vous acquitter de l'amende en plus de perdre toutes les
livraisons faites par le livreur</p>

<h3>La notoriété</h3>
<p>La notoriété est une compilation de plusieurs score. D'abord, bien sûr, la qualité de vos pizzas. Puisque c'est vous
qui créez les recettes, chaque pizza est différente et aura une appréciation différente des clients. En second paramètre,
on retrouve le prix de celles ci. S'il est trop élevé, cela n'attirera personne, s'il est trop bas, les clients pourraient
se méfier. La notoriété est également basée sur les livraisons, de bonnes livraisons offrent une publicité gratuite à
votre enseigne. Enfin, la notoriété est affectée par votre comportement vis à vis de la concurrence: avoir trop souvent
recours aux voyous nuit à cette dernière; les clients ne vous font plus confiance.</p>
<p>La notoriété peut également être modifiée temporairement, en positif comme en négatif. Cela peut concerner votre restaurant
uniquement par un évènement personnel (visite d'une célébrité, attaque de votre restaurant) ou peut être un évènement global
qui affectera tous les restaurants.</p>
<p>Les évènements globaux sont gérés par l'admin et sont annoncés dans le Journal. Il peut arriver qu'ils soient annoncés
en avance dans la newsletter mensuelle.</p>

<h3>Duel de pizzas</h3>
<p>Vous pouvez mettre votre meilleure pizza en compétition avec d'autres pizzas. Pour cela vous devez soit sélectionner
la pizza que vous souhaitez faire juger, mettre la somme d'argent en jeu et attendre que quelqu'un accepte le duel,
soit choisir un restaurant concurrent qui a soumis sa pizza pour la compétition.</p>
<p>Un jury généré par le jeu désignera la pizza gagnante. Le restaurant qui gagne remporte l'argent. Vous ne pouvez
gagner qu'un seul duel par jour.</p>

<h3>Les voyous</h3>
<p>Avoir recours aux voyous pour freiner la concurrence est tout à fait possible dans le jeu. Cela comporte des risques à
connaître.</p>
<p>Les voyous ne sont pas vos employés, ils sont payés pour chaque mission que vous leur donnez.</p>

<p>Les vandalismes possible sont :</p>
    <ul>
        <li>Braquage : Les voyous s'emparent de la caisse enregistreuse (perte d'argent)</li>
        <li>Frayeur : Les voyous insultent les clients et squattent la devanture (moins de clients)</li>
        <li>Graffitis et jet de pierre : Les voyous brisent les vitres du restaurant et font des graffitis sur les murs
        (notoriété en berne)</li>
        <li>Coup et blessures : Les voyous attendent sagement que le meilleur employé du restaurant finissent sa journée
        et le tabasse sauvagement. (l'employé ciblé démissionne)</li>
    </ul>
<p>Il leur arrive d'échouer, le cas
échéant, vous ne récupérerez pas la somme d'argent investie (qui est saisie par la police) et vous devrez vous acquitter
d'une amende qui dépendra de la gravité du vandalisme. Vous perdrez également des points de notoriété. Et parfois,
ils ne font pas nécessairement ce que vous leur avez demandé (Frayeur au lieu de Graffiti par exemple), si cela
arrive et qu'ils échouent vous subirez la sanction judiciaire la plus forte.</p>
<p>Les voyous agissent aussi à leur compte et il leur arrive de racketter les commerçants. Ils vous menaçeront de leur payer
une assurance. Vous pouvez accepter ou refuser, si vous acceptez, vous vous acquiterez de la somme demandée. Si vous
refusez, vous risquez de subir un vandalisme au hasard. Vous pouvez également prévenir la police afin qu'ils renforcent
la sécurité près de votre magasin pendant la durée du racket. La présence de la police vous empêchera de passer un marché
avec les voyous.</p>
<p>Attention, veuillez noter que, même si vous avez l'aide de la police ou que vous avez payé l'assurance, cela n'empêchera
pas un autre joueur de vous attaquer.</p>

<h3>Alertes mails</h3>
<p>Vous pouvez décider de recevoir des alertes mails lorsqu'il arrive un évènement qui touche votre restaurant (vandalisme,
visite d'une célébrité, stock bientôt épuisé, démission etc). Pour se faire vous devez accepter de recevoir des offres
de nos partenaires par mail. Les alertes mails seront envoyées en accompagnement de ces offres ceci permettant de financer
le développement de ce jeu sans nécessairement demander aux joueurs de payer quoi que ce soit.</p>

<h3>La newsletter</h3>
<p>Une newsletter est mise en place et aura une fréquence inférieure à un mail par mois. La raison étant que la newsletter
ne servira unquement à prévenir d'un évènement global touchant l'enseble des joueurs ainsi que de publier les résultats
des meilleures pizzas du jeu.</p>